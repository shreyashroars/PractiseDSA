/**
 * Definition for singly-linked list.
 * class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) {
 *         val = x;
 *         next = null;
 *     }
 * }
 */
public class Solution {
    public ListNode detectCycle(ListNode head) {
        if(head==null)
            return null;
        ListNode slow=head;
        ListNode fast=head;
        ListNode temp=head;
       while(fast.next!=null && fast.next.next!=null)
       {
           slow=slow.next;
           fast=fast.next.next;
           if(fast==slow)
           {
               slow=head;
               while(slow!=fast)
               {
                   slow=slow.next;
                   fast=fast.next;
               }
               return fast;
           }
       }
        return null;
        
    }
}